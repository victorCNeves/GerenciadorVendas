    public void procurar() throws ValorNumericoInvalidoException{
        hideForm();
        if(cxId.getValue()!=null){
            m = bd.getMovel(((Number) cxId.getValue()).intValue());
            if(m!=null){
                panConMovel.setVisible(true);
                btAtualizar.setVisible(true);
                btExcluir.setVisible(true);
                switch (m.getClass().getName()) {
                    case "Geladeira" -> {
                        panConGeladeira.setVisible(true);
                        rtTitulo.setText("Consulta de Geladeira");
                    }
                    case "Mesa" -> {
                        panConMesa.setVisible(true);
                        rtTitulo.setText("Consulta de Mesa");
                    }
                    default -> {
                        panConGuardaRoupa.setVisible(true);
                        rtTitulo.setText("Consulta de Guarda Roupa");
                    }
                }
                valores();
                calculaLucro();
            }else{
               JOptionPane.showMessageDialog(null, "O ID inserido √© inv√°lido. Por favor, verifique e tente novamente", "Erro de Valida√ß√£o de ID", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Para realizar a pesquisa, √© necess√°rio inserir um ID no campo correspondente. Por favor, preencha o campo ID e tente novamente.", "Campo ID Vazio", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    private void valores(){
        cxNome.setText(m.getNome());
        cxQtdd.setValue(m.getQuantidadeEstoque());
        //Reflexividade
        cxValorCusto.setValue(m.getValor().getValorCusto());
        //Reflexividade
        cxValorVenda.setValue(m.getValor().getValorVenda());
        switch (m.getClass().getName()) {
            case "Geladeira" -> {
                Geladeira g = (Geladeira) m;
                cxCapacidadeGeladeira.setValue(g.getCapacidadeLitros());
                if(g.isFreezer()){
                    btFreezerSim.setSelected(true);
                }else{
                    btFreezerNao.setSelected(true);
                }
            }
            case "Mesa" -> {
                Mesa mesa = (Mesa) m;
                cxQtddLugaresMesa.setValue(mesa.getQuantidadeLugares());
                cxFormatoMesa.setText(mesa.getFormato());
            }
            default -> {
                GuardaRoupa g = (GuardaRoupa) m;
                cxQtddPortasGuardaRoupa.setValue(g.getQuantidadePortas());
                if(g.isEspelho()){
                    btEspelhoSim.setSelected(true);
                }else{
                    btEspelhoNao.setSelected(true);
                }
            }
        }
    }
    
    private void atualizar() throws ValorNumericoInvalidoException{
        if(verificaPreenchimento()){
            switch (m.getClass().getName()) {
                case "Geladeira" -> {
                    Geladeira geladeira = (Geladeira) m;
                    geladeira.setCapacidadeLitros(((Number) cxCapacidadeGeladeira.getValue()).intValue());
                    geladeira.setFreezer(btFreezerSim.isSelected());
                }
                case "GuardaRoupa" -> {
                    GuardaRoupa guardaRoupa = (GuardaRoupa) m;
                    guardaRoupa.setQuantidadePortas(((Number)cxQtddPortasGuardaRoupa.getValue()).intValue());
                    guardaRoupa.setEspelho(btEspelhoSim.isSelected());
                }
                default -> {
                    Mesa mesa = (Mesa) m;
                    mesa.setFormato(cxFormatoMesa.getText());
                    mesa.setQuantidadeLugares(((Number) cxQtddLugaresMesa.getValue()).intValue());
                }
            }
            m.setNome(cxNome.getText());
            m.setQuantidadeEstoque(((Number)cxQtdd.getValue()).intValue());
            //Reflexividade
            m.getValor().setValorCusto(valor.getValorCusto());
            //Reflexividade
            m.getValor().setValorVenda(valor.getValorVenda());
            bd.attMovel(m);
            JOptionPane.showMessageDialog(
                    null,
                    "Os dados do m√≥vel foram atualizados com sucesso\n"
                    +m.exibirInformacoes(),
                    "Atualiza√ß√£o Conclu√≠da",
                    JOptionPane.INFORMATION_MESSAGE);
            atualizarTabelaRelatorio();
            limparForm();
            hideForm();
        }else{
            JOptionPane.showMessageDialog(null, "Todos os campos do formul√°rio s√£o obrigat√≥rios. Por favor, preencha-os para continuar.", "Campos Obrigat√≥rios", JOptionPane.OK_OPTION);
        }
    }
    
    private boolean verificaPreenchimento(){
        if(cxId.getText().isBlank() || cxNome.getText().isBlank() || cxQtdd.getText().isBlank() || cxValorCusto.getText().isBlank() || cxValorVenda.getText().isBlank()){
                return false;
            }
        switch (m.getClass().getName()) {
            case "Geladeira" -> {
                if(cxCapacidadeGeladeira.getText().isBlank() || btGrupoFreezer.getSelection()==null){
                    return false;
                }
            }
            case "Mesa" -> {
                if(cxQtddLugaresMesa.getText().isBlank() || cxFormatoMesa.getText().isBlank()){
                    return false;
                }
            }
            default -> {
                if(cxQtddPortasGuardaRoupa.getText().isBlank() || btGrupoEspelho.getSelection()==null){
                    return false;
                }
            }
        }
        return true;
    }
    
