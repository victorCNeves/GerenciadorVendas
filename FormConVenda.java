    private void excluir() throws ValorNumericoInvalidoException{
        int escolha = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esta venda permanentemente? Esta a√ß√£o n√£o poder√° ser desfeita.", "Confirmar Exclus√£o", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if(escolha==0){
            bdVenda.rmvVenda(v.getId());
            atualizarTabelaRelatorio();
            limparForm();
            hideForm();
        }
    }
    
    private void atualizarTabelaRelatorio(){
        FormRelVenda.getInstance().valoresTabela();
    }    
    
    public void procurar(){
        if(cxId.getValue()!=null){
            v = bdVenda.getVenda(((Number) cxId.getValue()).intValue());
            if(v!=null){
                if(panConVenda.isVisible()){
                    hideForm();
                }
                hideForm();
                Object id = cxId.getValue();
                limparForm();
                cxId.setValue(id);
                valores();
            }else{
               JOptionPane.showMessageDialog(null, "O ID inserido √© inv√°lido. Por favor, verifique e tente novamente", "Erro de Valida√ß√£o de ID", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Para realizar a pesquisa, √© necess√°rio inserir um ID no campo correspondente. Por favor, preencha o campo ID e tente novamente.", "Campo ID Vazio", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    private void valores(){
        cxData.setValue(v.getData().format(DateTimeFormatter.ofPattern("dd/MM/uuuu")));
        cxVendedor.setValue(v.getVendedor().getId());
        rtNomeVendedor.setText(v.getVendedor().getNome());
        cxCliente.setValue(v.getCliente().getId());
        rtNomeCliente.setText(v.getCliente().getNome());
        for (ItemVenda item : v.getItensVenda()) {
            //Reflexividade
            modTab.addRow(new Object[]{item.getMovel().getNome(), item.getQuantidade(), item.getMovel().getValor().getValorVenda(), item.calcularValorTotal()});
        }
        cxValorTotalVenda.setValue(v.calcularValorTotal());
    }
    
    private void sair(){
        int escolha = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja sair da tela de consulta de vendas?", "Confirmar Sa√≠da", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if(escolha==0){
            limparForm();
            this.dispose();            
        }
    }
    
    private void limparForm(){
        cxId.setValue(null);
        cxCliente.setValue(null);
        cxData.setValue(null);
        cxId.setValue(null);
        cxValorTotalVenda.setValue(null);
        cxVendedor.setValue(null);
        rtNomeMovel.setText("");
        rtNomeCliente.setText("");
        rtNomeVendedor.setText("");
        modTab = (DefaultTableModel) tbMovel.getModel();
        modTab.setRowCount(0);
    }
    
    private void nomePessoa(boolean cliente) {
        Object valor = cliente ? cxCliente.getValue() : cxVendedor.getValue();

        if (valor != null) {
            int id = ((Number) valor).intValue();
            Pessoa p = bdPessoa.getPessoa(id);

            String nomeOuErro = (p != null) ? p.getNome() : "Id inv√°lido!";

            if (cliente) {
                rtNomeCliente.setText(nomeOuErro);
            } else {
                rtNomeVendedor.setText(nomeOuErro);
            }
        }
    }
    
    private void hideForm(){
        boolean visible = !panConVenda.isVisible();
        panConVenda.setVisible(visible);
        btExcluir.setVisible(visible);
    }
    
    public void init(){
        this.setVisible(true);
        hideForm();
        v = new Venda();
        modTab = (DefaultTableModel) tbMovel.getModel();
    }
       
